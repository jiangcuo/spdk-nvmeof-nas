const route = useRoute()
const router = useRouter()
const userStore = useUserStore()

// ‰æßËæπÊ†èÁä∂ÊÄÅ
const isCollapsed = ref(false)
const sidebarWidth = computed(() => isCollapsed.value ? '64px' : '200px')

// Á≥ªÁªüÁä∂ÊÄÅ
const systemStatus = ref({
  connected: false,
  loading: false
})

// ÂΩìÂâçË∑ØÁî±
const currentRoute = computed(() => route.path)

// ÈªòËÆ§Â±ïÂºÄÁöÑËèúÂçï
const defaultOpenedMenus = computed(() => {
  // Â¶ÇÊûúÂΩìÂâçË∑ØÁî±ÊòØÊüê‰∏™Â≠êËèúÂçïÈ°πÔºåÂàôÂ±ïÂºÄÂØπÂ∫îÁöÑÁà∂ËèúÂçï
  const currentPath = route.path
  const parentMenus = []
  
  menuItems.value.forEach(item => {
    if (item.children && item.children.some(child => child.path === currentPath)) {
      parentMenus.push(item.name)
    }
  })
  
  return parentMenus
})

const currentPageTitle = computed(() => {
  // ÂÖàÂú®Á¨¨‰∏ÄÂ±ÇËèúÂçï‰∏≠Êü•Êâæ
  let menuItem = menuItems.value.find(item => item.path === route.path)
  
  // Â¶ÇÊûúÊ≤°ÊâæÂà∞ÔºåÂàôÂú®Â≠êËèúÂçï‰∏≠Êü•Êâæ
  if (!menuItem) {
    for (const item of menuItems.value) {
      if (item.children) {
        menuItem = item.children.find(child => child.path === route.path)
        if (menuItem) break
      }
    }
  }
  
  return menuItem?.title || route.meta?.title || '‰ª™Ë°®Êùø'
})

// ËèúÂçïÈ°π
const menuItems = ref(getMenuItems())
const visibleMenuItems = computed(() => {
  return menuItems.value.filter(item => {
    // Â¶ÇÊûúÈúÄË¶ÅÁÆ°ÁêÜÂëòÊùÉÈôê‰ΩÜÁî®Êà∑‰∏çÊòØÁÆ°ÁêÜÂëòÔºåÂàôÈöêËóè
    if (item.requiresAdmin && !userStore.isAdmin) {
      return false
    }
    return true
  })
})

// ÂàáÊç¢‰æßËæπÊ†è
const toggleSidebar = () => {
  isCollapsed.value = !isCollapsed.value
  
  // ‰øùÂ≠òÂà∞Êú¨Âú∞Â≠òÂÇ®
  localStorage.setItem('sidebar-collapsed', isCollapsed.value.toString())
}

// Â§ÑÁêÜÁî®Êà∑ËèúÂçïÂëΩ‰ª§
const handleUserCommand = async (command) => {
  switch (command) {
    case 'profile':
      // TODO: ÊâìÂºÄ‰∏™‰∫∫ËµÑÊñôÂØπËØùÊ°Ü
      ElMessage.info('‰∏™‰∫∫ËµÑÊñôÂäüËÉΩÊ≠£Âú®ÂºÄÂèë‰∏≠')
      break
      
    case 'logout':
      await handleLogout()
      break
  }
}

// Â§ÑÁêÜÈÄÄÂá∫ÁôªÂΩï
const handleLogout = async () => {
  try {
    await ElMessageBox.confirm(
      'Á°ÆÂÆöË¶ÅÈÄÄÂá∫ÁôªÂΩïÂêóÔºü',
      'Á°ÆËÆ§ÈÄÄÂá∫',
      {
        type: 'warning',
        confirmButtonText: 'Á°ÆÂÆö',
        cancelButtonText: 'ÂèñÊ∂à'
      }
    )
    
    await userStore.logout()
    await router.push('/login')
    
  } catch (error) {
    // Áî®Êà∑ÂèñÊ∂àÈÄÄÂá∫
    if (error !== 'cancel') {
      console.error('ÈÄÄÂá∫ÁôªÂΩïÂ§±Ë¥•:', error)
    }
  }
}

// Ê£ÄÊü•Á≥ªÁªüÁä∂ÊÄÅ
const checkSystemStatus = async () => {
  try {
    systemStatus.value.loading = true
    const response = await ApiService.system.getStatus()
    systemStatus.value.connected = response.status === 200
  } catch (error) {
    systemStatus.value.connected = false
    console.error('Ê£ÄÊü•Á≥ªÁªüÁä∂ÊÄÅÂ§±Ë¥•:', error)
  } finally {
    systemStatus.value.loading = false
  }
}

// ÊÅ¢Â§ç‰æßËæπÊ†èÁä∂ÊÄÅ
onMounted(() => {
  const savedState = localStorage.getItem('sidebar-collapsed')
  if (savedState !== null) {
    isCollapsed.value = savedState === 'true'
  }
  
  // Ê£ÄÊü•Á≥ªÁªüÁä∂ÊÄÅ
  checkSystemStatus()
  
  // ÂÆöÊúüÊ£ÄÊü•Á≥ªÁªüÁä∂ÊÄÅ
  setInterval(checkSystemStatus, 30000) // ÊØè30ÁßíÊ£ÄÊü•‰∏ÄÊ¨°
})

// ÁõëÂê¨Ë∑ØÁî±ÂèòÂåñ
watch(route, () => {
  console.log(`üîó ÂΩìÂâçË∑ØÁî±: ${route.path}`)
})
</script>

